function soma(a, b) {
    return a + b;
}

function subtracao(a, b) {
    return a - b;
}

function multiplicacao(a, b) {
    return a * b;
}

function divisao(a, b) {
    return a / b;
}

function modulo(a, b) {
    return a % b;
}

console.log(soma(2, 5));
console.log(subtracao(7, 5));
console.log(multiplicacao(3, 3));
console.log(divisao(25, 5));
console.log(modulo(10, 5));
